def modulus(a,b):
    c=a%b
    print("The modulus of two numbers are : ",c)    

x=int(input("Enter the first integer num: "))
y=int(input("Enter the second integer num: "))
modulus(x,y)

def power(m,n):
    c=m**n
    print("The modulus of two numbers are : ",c)    

x=int(input("Enter the first integer num: "))
y=int(input("Enter the second integer num: "))
power(x,y)
